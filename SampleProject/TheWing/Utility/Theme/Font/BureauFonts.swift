    static func bureauGro35(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BureauGrotesque-ThreeFive", fontSize: fontSize)
    }

    static func bureauGro37(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BureauGrotesque-ThreeSeven", fontSize: fontSize)
    }

    static func bureauGroLight(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BureauGrot-Light", fontSize: fontSize)
    }

    static func bureauGroBold(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BureauGrotComp-Bold", fontSize: fontSize)
    }

}
