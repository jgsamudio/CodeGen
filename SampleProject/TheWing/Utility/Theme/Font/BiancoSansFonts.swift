import UIKit

struct BiancoSansFonts {

	// MARK: - Public Functions

    // MARK: - Public Functions
    
    static func biancoSansNew(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSansNew", fontSize: fontSize)
    }

    static func biancoSansNewItalic(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSansNew-Italic", fontSize: fontSize)
    }

    static func biancoSansNewHeavy(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSansNew-Heavy", fontSize: fontSize)
    }

    static func biancoSansNewHeavyItalic(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSansNew-HeavyItalic", fontSize: fontSize)
    }

    static func biancoSansNewExtraBold(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSansNew-ExtraBold", fontSize: fontSize)
    }

    static func biancoSansNewExtraBoldItalic(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSansNew-ExtraBoldItalic", fontSize: fontSize)
    }

    static func biancoSansNewBold(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSansNew-Bold", fontSize: fontSize)
    }

    static func biancoSansNewBoldItalic(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSansNew-BoldItalic", fontSize: fontSize)
    }

}
