import UIKit

struct TildaFonts {
    
    // MARK: - Public Functions
    
    static func tildaPetite(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "Tilda-Petite", fontSize: fontSize)
    }
    
}
