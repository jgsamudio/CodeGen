import UIKit

struct BiancoSerifFonts {

	// MARK: - Public Functions

    // MARK: - Public Functions
    
    static func biancoSerifNew(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSerifNew", fontSize: fontSize)
    }

    static func biancoSerifNewItalic(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSerifNew-Italic", fontSize: fontSize)
    }

    static func biancoSerifNewHeavy(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSerifNew-Heavy", fontSize: fontSize)
    }

    static func biancoSerifNewHeavyItalic(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSerifNew-HeavyItalic", fontSize: fontSize)
    }

    static func biancoSerifNewExtraBold(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSerifNew-ExtraBold", fontSize: fontSize)
    }

    static func biancoSerifNewExtraBoldItalic(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSerifNew-ExtraBoldItalic", fontSize: fontSize)
    }

    static func biancoSerifNewBold(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSerifNew-Bold", fontSize: fontSize)
    }

    static func biancoSerifNewBoldItalic(ofSize fontSize: CGFloat) -> UIFont {
        return UIFont.font(name: "BiancoSerifNew-BoldItalic", fontSize: fontSize)
    }

}
